from daliuren import get_san_chuan


def test_fu_yin():
    """测试伏吟：天盘地盘相同"""
    tian_pan = {dz: dz for dz in ['子','丑','寅','卯','辰','巳','午','未','申','酉','戌','亥']}
    si_ke = [
        ('第一课', '寅', '寅'),
        ('第二课', '午', '午'),
        ('第三课', '申', '申'),
        ('第四课', '子', '子'),
    ]
    result = get_san_chuan(si_ke, tian_pan, '甲')
    assert result['起传法'] == '伏吟', f"Expected 伏吟, got {result['起传法']}"
    assert result['初传'] == '寅', f"Expected 初传=寅(甲寄宫), got {result['初传']}"
    print("[OK] 伏吟测试通过")


