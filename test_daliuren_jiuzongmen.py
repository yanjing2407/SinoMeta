def test_zhi_yi():
    """测试知一/比用：多个贼克时取与日干阴阳同者"""
    tian_pan = {'子':'丑','丑':'寅','寅':'卯','卯':'辰','辰':'巳','巳':'午',
                '午':'未','未':'申','申':'酉','酉':'戌','戌':'亥','亥':'子'}

    # 构造两个贼克：子（阳）和丑（阴）
    si_ke = [
        ('第一课', '子', '未'),  # 贼：未(土)克子(水)，子是阳支
        ('第二课', '丑', '寅'),  # 贼：寅(木)克丑(土)，丑是阴支
        ('第三课', '寅', '寅'),  # 比和
        ('第四课', '卯', '卯'),  # 比和
    ]
    # 甲是阳干，应取阳支子
    result = get_san_chuan(si_ke, tian_pan, '甲')
    assert '知一' in result['起传法'] or '比用' in result['起传法'], f"Expected 知一/比用, got {result['起传法']}"
    assert result['初传'] == '子', f"Expected 初传=子(阳支), got {result['初传']}"
    print("[OK] 知一/比用测试通过")


