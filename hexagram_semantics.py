def _full_name_from_pair(shang: Optional[str], xia: Optional[str]) -> str:
    if not shang or not xia:
        return ""
    return GUA_64.get((str(shang), str(xia)), f"{shang}{xia}")


def normalize_hexagram_name(
    name: Optional[str] = None,
    shang: Optional[str] = None,
    xia: Optional[str] = None,
) -> str:
    """Return the full 64-hexagram name when possible."""
    pair_name = _full_name_from_pair(shang, xia)
    if pair_name:
        return pair_name
    text = str(name or "").strip()
    if text in COMMON_BY_NAME:
        return text
    if len(text) == 2 and text[0] in TRIGRAM_SEMANTICS and text[1] in TRIGRAM_SEMANTICS:
        return _full_name_from_pair(text[0], text[1])
    return text


def _generic_semantics(full_name: str, shang: str = "", xia: str = "") -> Dict[str, List[str]]:
    upper = TRIGRAM_SEMANTICS.get(shang, {})
    lower = TRIGRAM_SEMANTICS.get(xia, {})
    core = [full_name]
    relation = []
    risk = []
    if upper:
        core.append(f"外象为{upper['象']}，性主{upper['性']}")
        relation.extend(upper.get("关系", [])[:2])
        risk.extend(upper.get("风险", [])[:1])
    if lower:
        core.append(f"内象为{lower['象']}，性主{lower['性']}")
        relation.extend(lower.get("关系", [])[:2])
        risk.extend(lower.get("风险", [])[:1])
    return {
        "核心象意": core[:4],
        "关系语义": relation[:4] or ["需结合世应、体用和问题类型判断"],
        "事业语义": ["按上下卦内外关系判断推进方式"],
        "失物语义": ["按内外卦象定位环境与阻隔"],
        "趋势语义": ["趋势需结合动爻与变卦判断"],
        "风险标签": risk[:3] or ["语义需合参"],
    }


